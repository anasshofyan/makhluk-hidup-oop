class MakhlukHidup {
  String caraRespirasi;
  String caraBerkembangBiak;

  MakhlukHidup(this.caraRespirasi, this.caraBerkembangBiak);

  void setCaraRespirasi(caraRespirasi){
    this.caraRespirasi = caraRespirasi;
  }

  void setCaraBerkembangBiak(caraRespirasi){
    this.caraRespirasi = caraRespirasi;
  }

  getCaraRespirasi() {
    return this.caraRespirasi;
  }

  getCaraBerkembangBiak() {
    return this.caraBerkembangBiak;
  }
}
